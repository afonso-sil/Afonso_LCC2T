    private void login(String email, String senha){
        
        if(usuarioList.isEmpty()){
            JOptionPane.showMessageDialog(new JFrame(), "Nenhum resultado encontrado", "Dialog", JOptionPane.ERROR_MESSAGE);
            return;
        }
            
        for (projecto.models.Usuario usuario: usuarioList){
            if(usuario.getEmail().equals(email) && usuario.getSenha().equals(senha)){
                JOptionPane.showMessageDialog(new JFrame(), "logado com sucesso", "Dialog", JOptionPane.INFORMATION_MESSAGE);

                PrincipalView principalView = new PrincipalView();

                principalView.setVisible(true);
                this.setVisible(false);
                break;
            }else{
                   JOptionPane.showMessageDialog(new JFrame(), "usuario n찾o encontrado", "Dialog", JOptionPane.INFORMATION_MESSAGE);
            }
        }
       
    }
